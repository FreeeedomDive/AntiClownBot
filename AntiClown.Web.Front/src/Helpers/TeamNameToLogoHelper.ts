export function teamNameToLogo(teamName: string): string {
  if (teamName === "10 Alpine") {
    return "https://sportsmintmedia.com/wp-content/uploads/2022/02/Alpine-F1-team-announces-BWT-as-title-sponsor.jpg";
  }

  if (teamName === "07 Aston Martin") {
    return "https://cdn-5.motorsport.com/images/mgl/68yGpmX0/s200/logo-aston-martin.webp";
  }

  if (teamName === "09 Audi") {
    return "https://upload.wikimedia.org/wikipedia/commons/0/03/Audif1.com_logo17_%28cropped%29.svg";
  }

  if (teamName === "11 Cadillac") {
    return "https://upload.wikimedia.org/wikipedia/en/b/bc/Cadillac_Formula_1_Team_Logo_%282025%29.svg";
  }

  if (teamName === "04 Ferrari") {
    return "https://upload.wikimedia.org/wikipedia/en/d/df/Scuderia_Ferrari_HP_logo_24.svg";
  }

  if (teamName === "08 Haas") {
    return "https://cdn-4.motorsport.com/images/mgl/0ZRgKzM0/s200/haas-logo-1.webp";
  }

  if (teamName === "01 McLaren") {
    return "https://cdn-2.motorsport.com/images/mgl/YEQJgyLY/s200/mclaren-f1-team-logo-1.webp";
  }

  if (teamName === "02 Mercedes") {
    return "https://cdn-1.motorsport.com/images/mgl/0RrzmDo0/s200/mercedes-f1-logo-1.webp";
  }

  if (teamName === "06 Racing Bulls") {
    return "https://cdn-1.motorsport.com/images/mgl/Y998adGY/s200/visa-cash-app-rb-logo.webp";
  }

  if (teamName === "03 Red Bull Racing") {
    return "https://cdn-3.motorsport.com/images/mgl/Y99JQRbY/s200/red-bull-racing-logo-1.webp";
  }

  if (teamName === "05 Williams") {
    return "https://cdn-7.motorsport.com/images/mgl/2y39NBy6/s200/williams-racing-logo-1.webp";
  }

  return "";
}
